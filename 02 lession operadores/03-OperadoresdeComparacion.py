a = 4
b = 6

# IGUAL
resultado = a == b
print(f'Resultado de {a} == {b} : {resultado}')

# DIFERENTE
resultado = a != b
print(f'Resultado de {a} != {b} : {resultado}')

# MAYOR QUE
resultado = a > b
print(f'Resultado de {a} >  {b}: {resultado}')

# MAYOR IGUAL QUE
resultado = a >= b
print(f'Resultado de {a} >= {b} : {resultado}')

# MENOR QUE
resultado = a < b
print(f'Resultado de {a} <  {b}: {resultado}')

# MENOR IGUAL QUE
resultado = a <= b
print(f'Resultado de {a} <= {b}: {resultado}')

# los tipos de datos bool (boolean) son True u False
vVerdadera = True
vFalsa = False
print("Variable verdadera:", vVerdadera)
print("Variable falsa:", vFalsa)


# si hacemos una comparacion de mayor que o menor que nos mostrara la variable booleana que corresponta

Comparación = 3 > 1
print("Tres es mayor que uno:", Comparación)


# se puede usar la comparacion para dar resultados en condicional if
if Comparación:
  	print("La comparación es verdadera")
else:
  	print("La comparación es falsa")

